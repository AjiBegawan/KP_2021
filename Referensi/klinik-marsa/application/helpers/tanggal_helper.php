/**
 * Fungsi untuk membuat tanggal dalam format bahasa indonesia
 * @param void
 * @return string format tanggal sekarang (contoh: 22 Desember 2016)
 */
if (!function_exists('tanggal')) {
    function tanggal() {
    	date_default_timezone_set('Asia/Jakarta');
        $tanggal = Date("Y-m-d");
        return $tanggal;
    }
}