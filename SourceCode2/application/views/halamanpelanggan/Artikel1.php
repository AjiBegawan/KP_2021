	            <h2 class="mb-3">#1. Cara Masuk ke NFT Art</h2>
	              <ul class="my-4">
	              	<li><span class="ion-ios-checkmark-circle mr-2"></span>Membuat Crypto Digital Wallet</li>
					  	<p>Hal pertama yang diperlukan yaitu membayar platform untuk ‘mint’ NFT. Sebagian besar platform menggunakan pembayaran dalam Ethereum. Untuk membayar Ethereum, dibutuhkan yang Namanya digital wallet dengan mata uang cryptocurrency untuk menghubungkannya ke platform NFT yang dipilih. Terdapat beberapa digital wallet yang dapat digunakan pada aplikasi ataupun browser extension. </p>
					  	 <p>
	              			<img src="<?= base_url()?>assets/image/ewallet.jpg" alt="" class="img-fluid">
	           			</p>
					<li><span class="ion-ios-checkmark-circle mr-2"></span>Menentukan Blockchaine</li>
						<p>Proses pembuatan NFT baik itu GIF atau gambar cenderung mudah. Hal pertama yang harus dilakukan yaitu menentukan blockchain mana yang diinginkan. Blockchain yang popular diantaranya:</p>
						<p>1. Rantai Cerdas Binance (Binance Smart Chain), blockchain otonom yang berdasarkan bukti otoritas yang diintai, ini fokus pada penyediaan dukungan optimal untuk semua alat di Ethereum. </p>
						<p>2. Flow by Dapper Labs, blockchain yang cepat dan ramah pengembang yang dirancang sebagai dasar bagi generasi berikutnya dApps, game, asset digital, dan token yang tidak dapat dipertukarkan, memecahkan masalah skalabilitas tanpa sharding dengan menggunakan arsitektur unik yang memungkinkan peningkatan besar dalam kecepatan dan hasil sesuai dengan ACID (Atomicity, Consistency, Isolation, Durability). </p>
						<p>3. Tron, blockchain yang bertujuan untuk memastikan teknologi ini sesuai dengan penggunaan sehari – hari, memiliki kapasitas jaringan untuk proses 2.000 transaksi per detik, Tron ini berfokus pada pembagian konten dan hiburan.</p>
						<p>4. EOS, blockchain yang paling sederhana, memberikan tingkat skalabilitas bertujuan untuk meningkatkan pengalaman bagi pengguna dan bisnis, EOS memastikan bahwa jaringannya lebih mudah digunakan dan memberikan keamanan yang lebih besar.</P>
						<p>5. Tezos, blockchain yang memiliki fitur bukti consensus andil, verifikasi formal, dan kemampuan untuk memungkinkan pemangku kepentingan memilih perubahan pada protokol </P>
						<p>6. Kosmos, blockchain yang dirancang untuk menskalakan dan berinteroperasi satu sama lain, pemegang dapat menaruh tokennya untuk memelihara jaringan dan menerima lebih banyak kosmos sebagai imbalan.</p>
						<p>
	              			<img src="<?= base_url()?>assets/image/Blockchain.jpg" alt="" class="img-fluid">
	           			</p>
						<p>Setiap blockchain memiliki standar token NFTnya tersendiri maka bijaklah dalam memilih blockchain yang akan digunakan. Karya hanya dapat dijual di platform yang mendukung blockchain yang sama. Karena Ethereum memiliki ekosistem NFT terbesar, inilah yang diperlukan untuk membuat karya seni, musik, atau video NFT sendiri di blockchain Ethereum. </p>
						
						
	              	<li><span class="ion-ios-checkmark-circle mr-2"></span>Mendaftar di Pasar </li>
						<p>Terdapat beberapa platform yang berpusat pada NFT yang memungkinkan untuk menguhubungkan digital wallet dan mengunggah file pilihan yang ingin diubah menjadi NFT. Pasar utama Ethereum NFT diantaranya adalah OpenSea dan Rarible</p>
					  	<p>
	              			<img src="<?= base_url()?>assets/image/Market.jpg" alt="" class="img-fluid">
	           			</p>
						<p> MakersPlace juga memungkinkan untuk membuat NFT, tetapi harus mendaftar untuk menjadi artis terlebih dahulu. Pada pasar OpenSea, tidak ada biaya, namun ada beberapa platform yang mengenakan biaya. Biaya tersebut disebut ‘gas’. Gas Ethereum merupakan sejumlah ether yang diperlukan untuk menjalankan beberapa fungsi blockchain, biaya bervariasi tergantung kemacetan jaringan. Semakin tinggi jumlah nilai orang yang bertransaksi pada jaringan tersebut, akan semakin tinggi harga gas begitupun sebaliknya.</p>
					<li><span class="ion-ios-checkmark-circle mr-2"></span>Membuat NFT</li>
						<p>Klik “create” pada top menu. Setelah itu, masukan beberapa informasi yang diperlukan, lalu simpan. Proses pencetakan NFT baru sudah siap. Klik Item Baru, muat karya seni, dan berikan semua detail yang ingin diberikan. Saat semuanya telah siap, klik “create”. </p>
